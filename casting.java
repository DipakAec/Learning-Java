public class casting {
    public static void main(String[] args) {
        double myDouble = 9.78;
        int myInt = (int) myDouble; // Manual casting: double to int
        int a = 9;
        double b = (double) a;
        System.out.println(b);
        System.out.println(myDouble);
        System.out.println(myInt);
    }
}
